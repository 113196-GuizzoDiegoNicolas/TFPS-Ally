package Ally.Scafolding.models;


/**
 * Enum representing a contact type.
 */

public enum ContactType {

    /**
     * Represents an email contact type.
     * This type is used when the contact information is an email address.
     */
    EMAIL,

    /**
     * Represents a phone number contact type.
     * This type is used when the contact information is a phone number.
     */
    PHONE,

    /**
     * Represents a social media link contact type.
     * Used when the contact information is a URL to a social media profile.
     */
    SOCIAL_MEDIA_LINK
}
